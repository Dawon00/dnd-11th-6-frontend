// 테스트 코드를 테스트 하기 위한 함수 작성. 추후 삭제 예정

/**
 * 두 숫자의 합을 반환합니다.
 */
export const add = (a: number, b: number): number => a + b

/**
 * 두 숫자의 차를 반환합니다.
 */
export const subtract = (a: number, b: number): number => a - b

/**
 * 주어진 숫자가 짝수인지 확인합니다.
 */
export const isEven = (num: number): boolean => num % 2 === 0
